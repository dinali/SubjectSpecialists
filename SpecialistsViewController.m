- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
	/*
	 If the requesting table view is the search display controller's table view, return the count of
     the filtered list, otherwise return the count of the main list.
	 */
	if (tableView == self.searchDisplayController.searchResultsTableView)
	{
        return [self.filteredListContent count];
    }
	else
	{
        return [self.listContent count];
    }
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"specialistCell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    /*
	 If the requesting table view is the search display controller's table view, configure the cell using the filtered content, otherwise use the main list.
	 */
	Specialist *specialist = nil;
	if (tableView == self.searchDisplayController.searchResultsTableView)
	{
        specialist = [self.filteredListContent objectAtIndex:indexPath.row];
    }
	else
	{
        specialist = [self.listContent objectAtIndex:indexPath.row];
    }
	
	cell.textLabel.text = specialist.name;
    cell.detailTextLabel.text = specialist.title;
    return cell;
}

